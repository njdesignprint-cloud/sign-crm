    async function importBackupJson(file) {
      if (!canImportBackup()) return showToast("Tu rol no puede importar respaldos.");
      if (!file) return;
      try {
        const text = await file.text();
        const data = JSON.parse(text);

        const clients = Array.isArray(data.clients) ? data.clients : [];
        const jobs = Array.isArray(data.jobs) ? data.jobs : [];
        const expenses = Array.isArray(data.expenses) ? data.expenses : [];
        const recurring = Array.isArray(data.recurringExpenses) ? data.recurringExpenses : [];
        const inventory = Array.isArray(data.inventoryItems) ? data.inventoryItems : [];
        const movements = Array.isArray(data.inventoryMovements) ? data.inventoryMovements : [];

        if (!clients.length && !jobs.length && !expenses.length && !recurring.length && !inventory.length && !movements.length) {
          return showToast("Ese JSON no tiene datos válidos.");
        }

        const ok = confirm(
          `Se van a importar:\n` +
          `Clientes: ${clients.length}\n` +
          `Trabajos: ${jobs.length}\n` +
          `Gastos: ${expenses.length}\n` +
          `Recurrentes: ${recurring.length}\n\n` +
          `Esto puede duplicar datos si ya los importaste antes.\n¿Quieres continuar?`
        );

        if (!ok) return;

        const clientMap = {};

        for (const item of clients) {
          const payload = {
            name: item.name || "",
            company: item.company || "",
            phone: item.phone || "",
            email: item.email || "",
            address: item.address || "",
            city: item.city || "",
            notes: item.notes || "",
            createdAt: firebase.firestore.FieldValue.serverTimestamp(),
            updatedAt: firebase.firestore.FieldValue.serverTimestamp()
          };
          const doc = await clientsRef().add(payload);
          if (item.id) clientMap[item.id] = doc.id;
        }

        for (const item of jobs) {
          const payload = {
            clientId: clientMap[item.clientId] || item.clientId || "",
            title: item.title || "",
            status: item.status || "Cotización",
            date: item.date || today(),
            dueDate: item.dueDate || "",
            priority: item.priority || "Media",
            sale: Number(item.sale || 0),
            description: item.description || "",
            notes: item.notes || "",
            materials: Array.isArray(item.materials) ? item.materials : [],
            quote: item.quote || { items: [], discountType: "none", discountValue: 0, taxPercent: 0 },
            checklist: item.checklist || {},
            payments: Array.isArray(item.payments) ? item.payments : [],
            internalNotesLog: Array.isArray(item.internalNotesLog) ? item.internalNotesLog : [],
            activityLog: Array.isArray(item.activityLog) ? item.activityLog : [newLogEntry("importación", "Trabajo importado desde respaldo JSON.")],
            createdAt: firebase.firestore.FieldValue.serverTimestamp(),
            updatedAt: firebase.firestore.FieldValue.serverTimestamp()
          };
          await jobsRef().add(payload);
        }

        for (const item of expenses) {
          const payload = {
            concept: item.concept || "",
            category: item.category || "Operación",
            amount: Number(item.amount || 0),
            date: item.date || today(),
            notes: item.notes || "",
            jobId: item.jobId || "",
            photos: Array.isArray(item.photos) ? item.photos : [],
            recurringId: item.recurringId || null,
            generatedMonth: item.generatedMonth || null,
            autoGenerated: !!item.autoGenerated,
            createdAt: firebase.firestore.FieldValue.serverTimestamp(),
            updatedAt: firebase.firestore.FieldValue.serverTimestamp()
          };
          await expensesRef().add(payload);
        }

        for (const item of recurring) {
          const payload = {
            concept: item.concept || "",
            category: item.category || "Operación",
            amount: Number(item.amount || 0),
            dayOfMonth: Number(item.dayOfMonth || 1),
            notes: item.notes || "",
            active: item.active !== false,
            lastGeneratedMonth: item.lastGeneratedMonth || "",
            createdAt: firebase.firestore.FieldValue.serverTimestamp(),
            updatedAt: firebase.firestore.FieldValue.serverTimestamp()
          };
          await recurringRef().add(payload);
        }

        const inventoryMap = {};
        for (const item of inventory) {
          const payload = {
            name: item.name || "",
            sku: item.sku || "",
            category: item.category || "Otro",
            unit: item.unit || "pieza",
            stock: Number(item.stock || 0),
            minStock: Number(item.minStock || 0),
            unitCost: Number(item.unitCost || 0),
            location: item.location || "",
            supplier: item.supplier || "",
            notes: item.notes || "",
            lastMovementAt: item.lastMovementAt || "",
            createdAt: firebase.firestore.FieldValue.serverTimestamp(),
            updatedAt: firebase.firestore.FieldValue.serverTimestamp()
          };
          const doc = await inventoryRef().add(payload);
          if (item.id) inventoryMap[item.id] = doc.id;
        }

        for (const item of movements) {
          const payload = {
            itemId: inventoryMap[item.itemId] || item.itemId || "",
            itemName: item.itemName || "",
            itemSku: item.itemSku || "",
            type: item.type || "ajuste",
            qty: Number(item.qty || 0),
            beforeStock: Number(item.beforeStock || 0),
            afterStock: Number(item.afterStock || 0),
            date: item.date || today(),
            reference: item.reference || "",
            note: item.note || "",
            jobId: item.jobId || "",
            unitCost: Number(item.unitCost || 0),
            createdBy: item.createdBy || state.userEmail || "usuario",
            createdAt: firebase.firestore.FieldValue.serverTimestamp()
          };
          await inventoryMovementsRef().add(payload);
        }

        showToast("Importación completada.");
      } catch (error) {
        console.error(error);
        showToast("No se pudo importar el JSON.");
      } finally {
        $("importJsonInput").value = "";
      }
    }
    async function removeItem(type, id, label) {
      if (!guardDelete(`eliminar ${label}`, moduleName)) return;
      if (!confirm(`¿Seguro que quieres eliminar este ${label}?`)) return;

      try {
        if (type === "clients") await clientsRef().doc(id).delete();
        if (type === "jobs") await jobsRef().doc(id).delete();
        if (type === "expenses") await expensesRef().doc(id).delete();
        if (type === "recurring") await recurringRef().doc(id).delete();
        if (type === "inventory") await inventoryRef().doc(id).delete();
        if (type === "providers") await providersRef().doc(id).delete();
        if (type === "purchaseOrders") await purchaseOrdersRef().doc(id).delete();
        showToast(`${label} eliminado.`);
      } catch (error) {
        console.error(error);
        showToast(`No se pudo eliminar ${label}.`);
      }
    }
